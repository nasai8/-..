public class MapData {

    char[][] map = {
            {'#','#','#','#','#','#','#','#','#'},
            {'#',' ',' ',' ','#',' ',' ',' ','#'},
            {'#',' ','#',' ','#',' ','#',' ','#'},
            {'#',' ','#',' ',' ',' ','#',' ','#'},
            {'#',' ','#','#','#',' ','#',' ','#'},
            {'#',' ',' ',' ',' ',' ',' ',' ','#'},
            {'#','#','#','#','#','#','#','#','#'}
    };

    int rows = map.length;
    int cols = map[0].length;

    boolean isFree(int x, int y) {
        return x >= 0 && y >= 0 &&
                x < rows && y < cols &&
                map[x][y] != '#';
    }
}
